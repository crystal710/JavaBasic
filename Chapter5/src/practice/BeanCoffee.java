package practice;

public class BeanCoffee {
	int money;
	 public String coffee(int money) {
		 
		 this.money += money;
		 if(money == Menu.BEANAMERICANO) {
			 return "콩다방 아메리카노를 구매하셨습니다.";
		 }
		 else if(money == Menu.BEANLATTE) {
			 return "콩다방 라테를 구매하셨습니다.";
		 }
		 else {
			 return null;
		 }
	 }
}
